def generate_poem(emotion):
    if emotion == "Sad":
        return "Even in sorrow, skies still glow,\nHold on tight, let the storm go."
    elif emotion == "Happy":
        return "Joy in the air, light in your eyes,\nDancing beneath open skies."
    else:
        return "A moment of calm, a silent breeze,\nEmotions rest with graceful ease."
